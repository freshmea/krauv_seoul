def main():
    a = int(input("input Number : "))
    # < > <= >= not and or 
    if a > 10:
        print(" a is bigger than 10")
    elif a > 5:
        print(" a is bigger than 5")
    elif a > 0:
        print(" a is bigger than 0")
    else:
        print(" a is less than 0")

    print('end of program')

if __name__ == '__main__':
    main()
